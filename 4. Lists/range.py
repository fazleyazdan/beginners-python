# range is off_by_one.
# it means it does not count the last value given to them
# for e.g. range(1,5) prints from 1 to 4

for value in range(1,5):
    print(value)
    
make_list = list(range(1,6))

print(make_list)

for value in range(6):                      # range(6) start from 0
    print(value)
    
