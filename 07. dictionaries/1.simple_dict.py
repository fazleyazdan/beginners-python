# A dictionary consist of key value pair
# A dictionary in Python is a collection of key-value pairs. Each key is connected to a value, 
# and you can use a key to access the value associated with that key. 
# A keyâ€™s value can be a number, a string, a list, or even another dictionary. 
# In fact, you can use any object that you can create in Python as a value in a dictionary.
# in the below example 'name' is key and 'audi' is value

dictionary = {'car': 'audi', 'model': '2024'}

# printing a dictionary
print(dictionary)

# printing value of key in a dictionary
print(dictionary['car'])
print(dictionary['model'])

